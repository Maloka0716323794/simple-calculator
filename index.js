function add(x, y) {
    let sum = x + y;
    console.log(x+ " + "+y+ " = "+sum);
}
add(5, 3);

// Substraction
function subtract(x,y)
{
    let difference= x-y;
    console.log(x+ " - "+y+ " = " +difference);
}
subtract(20,7);


//Multiplication
function multiply(x,y)
{
    let product= x*y;
    console.log(x+ " * "+y+ " = "+product);
}
multiply(2,4)

 //division
 function divide(x,y)
{
    let quotient= x/y;
    console.log(x+ " / "+y+ " = "+quotient);
}
divide(8,4)

